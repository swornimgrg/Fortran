C		PROGRAM AREA_TRI

		PROGRAM AREA
		REAL A,B,C
		REAL RESULT

		WRITE (*,*) 'ENTER THE SIDES OF THE TRIANGLE:'
		READ *,A,B,C

		RESULT= AREAOFTRIANGLE(A,B,C)
		WRITE (*,*) 'AREA' ,RESULT
		END

		REAL FUNCTION AREAOFTRIANGLE(X,Y,Z)
		REAL X,Y,Z,S
		S=(X+Y+Z)/2
		AREAOFTRIANGLE= SQRT(S*(S-X)*(S-Y)*(S-Z))
		RETURN
		END